## Exercise 1: Person

#assigning value to key
person = {'first_name' : 'Jungwon', 
              'last_name' : 'Yang',
              'age' : '19',
              'city' : 'Seoul'}

#print information stored
print ("First name:")
print (person ['first_name']) #will display the value Jungwon

print ("\nLast name:")
print (person ['last_name']) #will display the value Yang

print ("\nAge:")
print (person ['age']) #will display the value 19

print ("\nCity they live in:")
print (person ['city']) #will display the value Seoul