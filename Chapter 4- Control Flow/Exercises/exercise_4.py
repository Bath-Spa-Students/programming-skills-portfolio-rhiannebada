## Exercise 4: Stages of Life

Age = 19 #set a value for variable Age
if Age<2:
    print ("You are a baby.")
elif Age<4:
    print ("You are a toddler.")
elif Age<13:
    print ("You are a kid.")
elif Age<20:
    print ("You are a teenager.")
elif Age<65:
    print ("You are an adult.")
else:
    print ("You are an elder.")