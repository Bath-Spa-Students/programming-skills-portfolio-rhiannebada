## Print even numbers using 'while' loop

#assign variable with the starting value 
number = 2

#use of 'while' loop
while number <= 10: #loop will continue as long as value of 'number' is less than or equal to 10
    print (number)
    number += 2 #value is incremented by 2 to get next even number
                #loop will continue until 'number' exceeds 10